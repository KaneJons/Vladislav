#include <iostream>  // Для работы с вводом и выводом
#include <malloc.h>  // Для работы с функциями выделения памяти
using namespace std; // Упрощение синтаксиса (например, cout вместо std::cout)

int main() {
    setlocale(LC_ALL, "Russian"); // Устанавливаем локаль для русского текста

    int* X, N; // Указатель для массива X и переменная для его размера

    // Ввод размера массива
    cout << "Введите размер массива N: ";
    cin >> N;

    // Выделение памяти для массива через calloc
    X = (int*)calloc(N, sizeof(int));

    // Ввод элементов массива
    cout << "Введите элементы массива:\n";
    for (int i = 0; i < N; i++) {
        cin >> *(X + i);
    }

    // Нахождение минимального элемента и его индекса
    int minIndex = 0;
    for (int i = 1; i < N; i++) {
        if (*(X + i) < *(X + minIndex)) {
            minIndex = i;
        }
    }

    // Вывод минимального элемента
    cout << "Минимальный элемент массива: " << *(X + minIndex) << endl;

    // Сортировка элементов до минимального индекса (пузырьковая сортировка)
    for (int i = 0; i < minIndex - 1; i++) {
        for (int j = 0; j < minIndex - i - 1; j++) {
            if (*(X + j) > *(X + j + 1)) {
                int temp = *(X + j);
                *(X + j) = *(X + j + 1);
                *(X + j + 1) = temp;
            }
        }
    }

    // Вывод массива после сортировки
    cout << "Массив после сортировки элементов до минимального:\n";
    for (int i = 0; i < N; i++) {
        cout << *(X + i) << " ";
    }
    cout << "\n";

    // Освобождение памяти
    free(X);

    return 0;
}
